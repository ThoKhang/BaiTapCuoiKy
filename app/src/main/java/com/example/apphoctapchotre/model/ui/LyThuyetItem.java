public class LyThuyetItem {
    private String tieuDe;
    private int diem;

    public LyThuyetItem(String tieuDe, int diem) {
        this.tieuDe = tieuDe;
        this.diem= diem;
    }

    public String getTieuDe() {
        return tieuDe;
    }

    public int getDiemThuong() {
        return diem;
    }


}
